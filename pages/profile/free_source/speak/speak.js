// Render source items
function renderSourceItems() {
    const container = document.getElementById('sourceContainer');
    
    speakSourceData.forEach(source => {
        const sourceCard = document.createElement('div');
        sourceCard.className = 'source-item';
        sourceCard.innerHTML = `
            <div class="source-image-wrapper">
                <img src="${source.image}" alt="${source.title}" class="source-image">
                <span class="source-type ${source.type}">${source.type === 'free' ? 'Miễn phí' : 'Trả phí'}</span>
            </div>
            <h3 class="source-title" data-id="${source.id}">${source.title}</h3>
            <p class="source-description">${source.description}</p>
        `;
        
        // Add click event to title
        const titleElement = sourceCard.querySelector('.source-title');
        titleElement.addEventListener('click', () => {
            window.location.href = `./detail/source_detail.html?id=${source.id}`;
        });
        
        container.appendChild(sourceCard);
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    renderSourceItems();
});

